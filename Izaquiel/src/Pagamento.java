public class Pagamento
{
    private String data;
    private String tipoPagamento;
    private double valorPago;

    public String getData()
    {
        return this.data;
    }

    public String getTipoPagamento()
    {
        return this.tipoPagamento;
    }

    public double getValorPago()
    {
        return this.valorPago;
    }
}
